void main() {
  int grade = 4;

  switch (grade) {
    case 5:
      print('Өте жақсы');

      break;

    case 4:
      print('Жақсы');

      break;

    case 3:
      print('Қанағаттанарлық');

      break;

    case 2:
      print('Қанағаттанғысыз');

      break;

    default:
      print('Білмедім');
  }
}
